/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	uint8_t retval = 0;
	uint8_t dhcp_retry = 0;
	uint8_t dns_retry = 0;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI1_Init();
  MX_USART2_UART_Init();
  MX_TIM1_Init();
  /* USER CODE BEGIN 2 */
  printf("Hello World\r\n");

  wizchip_1ms_timer_initialize(repeating_timer_callback);
	
	wizchip_spi_initialize();
	wizchip_cris_initialize();
	wizchip_reset();
	wizchip_initialize();
	wizchip_check();

	if (g_net_info.dhcp == NETINFO_DHCP) // DHCP
	{
			wizchip_dhcp_init();
	}
	else // static
	{
			network_initialize(g_net_info);

			/* Get network information */
			print_network_information(g_net_info);
	}

	DNS_init(SOCKET_DNS, g_ethernet_buf);
	g_dns_get_ip_flag = 0;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
		
		/* Assigned IP through DHCP */
		if (g_net_info.dhcp == NETINFO_DHCP)
		{
				retval = DHCP_run();

				if (retval == DHCP_IP_LEASED)
				{
						if (g_dhcp_get_ip_flag == 0)
						{
								printf(" DHCP success\n");

								g_dhcp_get_ip_flag = 1;
						}
				}
				else if (retval == DHCP_FAILED)
				{
						g_dhcp_get_ip_flag = 0;
						dhcp_retry++;

						if (dhcp_retry <= DHCP_RETRY_COUNT)
						{
								printf(" DHCP timeout occurred and retry %d\n", dhcp_retry);
						}
				}

				if (dhcp_retry > DHCP_RETRY_COUNT)
				{
						printf(" DHCP failed\n");

						DHCP_stop();

						while (1)
								;
				}

				wizchip_delay_ms(1000); // wait for 1 second
				printf("g_dns_get_ip_flag = %d, retval = %d\r\n", g_dns_get_ip_flag, retval);
		}

		/* Get IP through DNS */
		if ((g_dns_get_ip_flag == 0) && (retval == DHCP_IP_LEASED))
		{
				while (1)
				{
						if (DNS_run(g_net_info.dns, g_dns_target_domain, g_dns_target_ip) > 0)
						{
								printf(" DNS success\n");
								printf(" Target domain : %s\n", g_dns_target_domain);
								printf(" IP of target domain : %d.%d.%d.%d\n", g_dns_target_ip[0], g_dns_target_ip[1], g_dns_target_ip[2], g_dns_target_ip[3]);

								g_dns_get_ip_flag = 1;

								break;
						}
						else
						{
								dns_retry++;

								if (dns_retry <= DNS_RETRY_COUNT)
								{
										printf(" DNS timeout occurred and retry %d\n", dns_retry);
								}
						}

						if (dns_retry > DNS_RETRY_COUNT)
						{
								printf(" DNS failed\n");

								while (1)
										;
						}

						wizchip_delay_ms(1000); // wait for 1 second
				}
		}
  }
  /* USER CODE END 3 */
}

